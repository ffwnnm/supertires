/**
 *
 * @author kilrwhle
 */
@Entity
@Table(name = "users")
public class User implements Serializable {

    @Id
    private Long id;
    @Column(name = "username")
    private String name;
    
    public User() {
    }

    public User(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
